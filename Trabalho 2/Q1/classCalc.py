class Calculadora():
    def soma(self, op1, op2):
        return float(op1) + float(op2)

    def sub(self, op1, op2):
        return float(op1) - float(op2)

    def mult(self, op1, op2):
        return float(op1) * float(op2)
    
    def div(self, op1, op2):  
        return float(op1) / float(op2)
        
